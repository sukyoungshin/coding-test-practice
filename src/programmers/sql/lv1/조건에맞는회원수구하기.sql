-- 1번 풀이
SELECT COUNT(USER_ID) AS USERS
  FROM USER_INFO
  WHERE AGE BETWEEN 20 AND 29
  AND JOINED BETWEEN TO_DATE(20210101, 'YYYY-MM-DD') AND TO_DATE(20211231, 'YYYY-MM-DD');

-- 2번 풀이
SELECT COUNT(USER_ID) AS USERS
  FROM USER_INFO
  WHERE AGE BETWEEN 20 AND 29
  AND TO_CHAR(JOINED, 'YYYY-MM-DD') BETWEEN '2021-01-01' AND '2021-12-31'

